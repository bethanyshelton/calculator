// basic calculations

// add
function add(num1, num2) {
	return num1 + num2;
}

//multiply
function multiply(num1, num2) {
	return num1 * num2;
}

//subtract
function subtract(num1, num2) {
	return num1 - num2;
}

//divide
function divide(num1, num2) {
	return num1 / num2;
}

//calculate
function calculator(num1, num2, operator) {
	return operator(num1, num2);
}
